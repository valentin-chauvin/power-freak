      <h4>La production en "wall to wall"</h4>
      <p>
        Outre l'utilisation à 80% d'énergies renouvelables, la
        fabrication de Power Freak Energy Drink s'appuie sur le
        principe du « wall to wall ». Cela signifie que la production
        et le remplissage ont lieu sur le même site, ce qui fait des
        kilomètres de transport économisés. Étant donné que nous
        n'avons pas à transporter les canettes vides jusque dans
        l'usine de remplissage, l'empreinte écologique est
        significativement réduite !
      </p>
    </section>

